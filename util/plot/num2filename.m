function str = num2filename(x)
% *************************************************************************
% *************************************************************************
% *************************************************************************
%
% MAKE FILE NAME STRING FOR DECIMAL NUMBER
%
% Brent A. Wallace
%
% 2023-03-13
%
% *************************************************************************
% *************************************************************************
% *************************************************************************

% Convert to string
str = num2str(x);

% Replace decimal points '.' with letter 'p'
str = strrep(str, '.', 'p');

% Replace minus sign '-' with letter 'm'
str = strrep(str, '-', 'm');